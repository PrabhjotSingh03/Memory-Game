    var countdownTime = 30; // Initial countdown time
    var gameWon = false;
    var gameLost = false;

    movies.addEventListener("click", function (a) {
      if (a.target.classList.contains("movies")) {
        activateMovie(a.target.id);
        selection.classList.add("hidden");
        gameBoard.classList.remove("hidden");
      }
    });
    function activateMovie(movie) {
      for (let i = 0; i < 20; i++) {
        images.push(lib[movie][i]);
      }
    
      for (let i = 0; i < 20; i++) {
        var rand = Math.floor(Math.random() * images.length);
        img[i].innerHTML = "<img src='" + images[rand] + "' alt='image' class='hidden'>";
        images.splice(rand, 1);
      }
    }
    gameBoard.addEventListener("click", gameLogic);
    function gameLogic(a) {
      if (a.target.classList.contains("flip")) {
        a.target.firstChild.classList.remove("hidden");
        if (click < 1) {
          if (click === -1) {
            timer = setInterval(function () {
              if (countdownTime > 0) {
                countdownTime--;
                gametime.innerHTML = countdownTime;
              } else {
                clearInterval(timer);
                if (!gameWon) {
                  showGameOver(); // Call the function to display "Game Over"
                }
              }
            }, 1000);
          }
          click = 1;
          temp1 = a.target;
        }  
        else if 
        (a.target !== temp1) {
          temp2 = a.target;
          if (temp1.firstChild.src !== temp2.firstChild.src) {
            gameBoard.removeEventListener("click", gameLogic);
            setTimeout(function () {
              temp1.firstChild.classList.add("hidden");
              temp2.firstChild.classList.add("hidden");
              gameBoard.addEventListener("click", gameLogic);
            }, 400);
            if (score > 0) {
              score -= 2;
            }
            gamescore.innerHTML = score;
          } else {
            score += 10;
            win += 2;
            temp1.firstChild.classList.add("outline");
            temp2.firstChild.classList.add("outline");
            temp1.classList.remove("flip");
            temp2.classList.remove("flip");
            score.innerHTML = score;
            if (win === 20) {
              clearInterval(timer);
              gameWon = true; // Set the flag to indicate game won
              resultFinal.innerHTML = "You win " + "with " + score + " points ";
              result.classList.remove("hidden");
            }
          }
          click = 0;
        }
      }
    }
    function showGameOver() {
      clearInterval(timer);
      resultFinal.innerHTML = "You lost"; // Display "You lost" in the result section
      result.classList.remove("hidden");
    }    

